package de.dosmike.sponge.WarCraftMC.exceptions;

public class ActionBuilderException extends Exception {
	public ActionBuilderException() {
		super();
	}
	public ActionBuilderException(String message) {
		super(message);
	}
	public ActionBuilderException(Throwable throwable) {
		super(throwable);
	}
	public ActionBuilderException(String message, Throwable throwable) {
		super(message, throwable);
	}
}
