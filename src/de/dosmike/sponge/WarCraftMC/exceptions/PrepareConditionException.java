package de.dosmike.sponge.WarCraftMC.exceptions;

@SuppressWarnings("serial")
public class PrepareConditionException extends Exception {
	public PrepareConditionException() {
		super();
	}
	public PrepareConditionException(String message) {
		super(message);
	}
	public PrepareConditionException(Throwable throwable) {
		super(throwable);
	}
	public PrepareConditionException(String message, Throwable throwable) {
		super(message, throwable);
	}
	
}
