package de.dosmike.sponge.WarCraftMC.exceptions;

@SuppressWarnings("serial")
public class PrepareOutputException extends Exception {
	public PrepareOutputException() {
		super();
	}
	public PrepareOutputException(String message) {
		super(message);
	}
	public PrepareOutputException(Throwable throwable) {
		super(throwable);
	}
	public PrepareOutputException(String message, Throwable throwable) {
		super(message, throwable);
	}
	
}
