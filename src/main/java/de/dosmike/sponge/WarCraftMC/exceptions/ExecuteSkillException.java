package de.dosmike.sponge.WarCraftMC.exceptions;

@SuppressWarnings("serial")
public class ExecuteSkillException extends Exception {
	public ExecuteSkillException() {
		super();
	}
	public ExecuteSkillException(String message) {
		super(message);
	}
	public ExecuteSkillException(Throwable throwable) {
		super(throwable);
	}
	public ExecuteSkillException(String message, Throwable throwable) {
		super(message, throwable);
	}
	
}
